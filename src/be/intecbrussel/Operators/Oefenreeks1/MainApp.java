package be.intecbrussel.Operators.Oefenreeks1;

public class MainApp {


    public static void main(String[] args) {

        // Operators - Oefenreeks1

        int a = 5;
        int b = 10;
        // Gebruik de plus operator (+) om de som van "a" en "b" te berekenen en print het resultaat.
        int sum = a + b;
        System.out.println("waarde : " + sum);

        // Gebruik de min operator (-) om het verschil tussen "a" en "b" te berekenen en print het resultaat.
        int min = a - b;
        System.out.println("min : " + min);

        // Gebruik de maal operator (*) om het product van "a" en "b" te berekenen en print het resultaat.
        int multiplication = a * b;
        System.out.println("multiplication : " + multiplication);

        // Gebruik de gedeeld door operator (/) om het quotient van "a" en "b" te berekenen en print hetresultaat
        int division = a / b;
        System.out.println("division : " + division);

        // Gebruik de modulo operator (%) om de rest van "a" gedeeld door "b" te berekenen en print hetresultaat.
        int rest = a % b;
        System.out.println("rest : " + rest);

        // Gebruik de increment operator (++) om de waarde van "a" te verhogen met 1 en print het resultaat.
        int incrementation = ++a;
        System.out.println("incrementation ++a : " + incrementation);

        // Gebruik de decrement operator (--) om de waarde van "a" te verlagen met 1 en print het resultaat.
        int decrementation = --a;
        System.out.println("decrementation --a : " + decrementation);

        //Gebruik de gelijk aan vergelijkings operator (==) om te controleren of "a" gelijk is aan "b" en print hetresultaat.
        boolean equal = a == b;
        System.out.println("a equal b : " + equal);

        // Gebruik de niet gelijk aan vergelijkings operator (!=) om te controleren of "a" ongelijk is aan "b" enprint het resultaat.
        boolean notEqual = a != b;
        System.out.println("a not equal b : " + notEqual);


    }


}
